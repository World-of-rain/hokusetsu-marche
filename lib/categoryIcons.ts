export function getItemEmoji(name: string, category?: string): string {
  for (const [keywords, emoji] of NAME_ICONS) {
    if (keywords.some((k) => name.includes(k))) return emoji;
  }
  if (category) {
    for (const [keywords, emoji] of CATEGORY_ICONS) {
      if (keywords.some((k) => category.includes(k))) return emoji;
    }
  }
  return "🛒";
}
